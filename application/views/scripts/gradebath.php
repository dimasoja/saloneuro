var animateSwitch = {
    switch: function (id) {
        $('.bodystep').fadeOut();
        $('.step' + id + '-body').fadeIn();
    }
}

var Controller = Backbone.Router.extend({
    routes: {
        "": "start",
        "!/step:hash/:productid": "switchProduct",
        "!/step:hash": "stepRoute"
    },
    start: function () {
        if (Steps != null) {
            Steps.render('1');
        }
        if (Step1 != null) {
            Step1.render();
        }

    },
    switchProduct: function (hash, productid) {
        if (Steps != null) {
            Steps.render(hash);
        }
        Step1.reRender(productid);
    },
    stepRoute: function (hash) {
        if (Steps != null) {
            Steps.render(hash);
        }
        if (hash == '1') {
            if (Step1 != null) {
                Step1.render();
            }
        }
        if (hash == '2') {
            if (Step2 != null) {
                Step2.render();
            }
        }
        if (hash == '3') {
            if (Step3 != null) {
                Step3.render();
            }
        }
        if (hash == '4') {
            if (Step4 != null) {
                Step4.render();
            }
        }
        if(Order != null) {
            Order.render();
        }
    }
});

